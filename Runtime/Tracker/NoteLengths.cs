namespace LiteNinja.MusicForge
{
  /// <summary>
  /// Enumeration of different note lengths.
  /// </summary>
  public enum NoteLengths
  {
    /// <summary>
    /// Semibreve note length (whole note)
    /// </summary>
    Semibreve,
    /// <summary>
    /// Dotted minim note length (dotted half note)
    /// </summary>
    DottedMinim,
    /// <summary>
    /// Minim note length (half note)
    /// </summary>
    Minim,
    /// <summary>
    /// Dotted crotchet note length (dotted quarter note)
    /// </summary>
    DottedCrotchet,
    /// <summary>
    /// Crotchet note length (quarter note)
    /// </summary>
    Crotchet,
    /// <summary>
    /// Dotted quaver note length (dotted eighth note)
    /// </summary>
    DottedQuaver,
    /// <summary>
    /// Quaver note length (eighth note)
    /// </summary>
    Quaver,
    /// <summary>
    /// Dotted semiquaver note length (dotted sixteenth note)
    /// </summary>
    DottedSemiquaver,
    /// <summary>
    /// Semiquaver note length (sixteenth note)
    /// </summary>
    Semiquaver,
    /// <summary>
    /// Dotted demisemiquaver note length (dotted thirty-second note)
    /// </summary>
    DottedDemisemiquaver,
    /// <summary>
    /// Demisemiquaver note length (thirty-second note)
    /// </summary>
    Demisemiquaver,
    /// <summary>
    /// Dotted semibreve note length (dotted whole note)
    /// </summary>
    DottedSemibreve,
    /// <summary>
    /// Breve note length (double whole note)
    /// </summary>
    Breve
  }

}