namespace LiteNinja.MusicForge.Instruments
{
  /// <summary>
  /// Enum representing the base notes of a musical scale.
  /// </summary>
  public enum BaseNote
  {
    /// <summary>
    /// Represents the note "C".
    /// </summary>
    C = 0,
    /// <summary>
    /// Represents the note "C#" or "Db".
    /// </summary>
    CSharp = 1,
    /// <summary>
    /// Represents the note "D".
    /// </summary>
    D = 2,
    /// <summary>
    /// Represents the note "D#" or "Eb".
    /// </summary>
    DSharp = 3,
    /// <summary>
    /// Represents the note "E".
    /// </summary>
    E = 4,
    /// <summary>
    /// Represents the note "F".
    /// </summary>
    F = 5,
    /// <summary>
    /// Represents the note "F#" or "Gb".
    /// </summary>
    FSharp = 6,
    /// <summary>
    /// Represents the note "G".
    /// </summary>
    G = 7,
    /// <summary>
    /// Represents the note "G#" or "Ab".
    /// </summary>
    GSharp = 8,
    /// <summary>
    /// Represents the note "A".
    /// </summary>
    A = 9,
    /// <summary>
    /// Represents the note "A#" or "Bb".
    /// </summary>
    ASharp = 10,
    /// <summary>
    /// Represents the note "B".
    /// </summary>
    B = 11
  }
}